-- | _incs
-- わざわざ名前をつけるほどでもないし、数値であれば整数でなくてもよい。
--
-- >>> (1 +) <$> [3,1,4,1,5]
-- [4,2,5,2,6]
-- >>> map (1 +) [3,2,4,1,5]
-- [4,3,5,2,6]
-- >>> _incs [3,1,4,1,5]
-- [4,2,5,2,6]
--
_incs :: Num a => [a] -> [a]
_incs = map (1 +)
